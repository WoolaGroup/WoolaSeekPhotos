    // ── IRepository<Tag, int> ─────────────────────────────────────────────────

    async Task<Tag?> IRepository<Tag, int>.GetByIdAsync(int id, CancellationToken ct)
    {
        using var connection = _connectionFactory.CreateConnection();
        return await connection.QueryFirstOrDefaultAsync<Tag>(
            "SELECT Id, Name, Category, IsAutoGenerated, UsageCount, CreatedAt FROM Tags WHERE Id = @Id",
            new { Id = id });
    }

    async Task<int> IRepository<Tag, int>.InsertAsync(Tag entity, CancellationToken ct)
        => await GetOrCreateTagAsync(entity.Name, entity.Category, entity.IsAutoGenerated);

    async Task IRepository<Tag, int>.UpdateAsync(Tag entity, CancellationToken ct)
    {
        using var connection = _connectionFactory.CreateConnection();
        await connection.ExecuteAsync(
            "UPDATE Tags SET Name = @Name, Category = @Category WHERE Id = @Id",
            new { entity.Name, entity.Category, entity.Id });
    }

    async Task<bool> IRepository<Tag, int>.ExistsAsync(int id, CancellationToken ct)
    {
        using var connection = _connectionFactory.CreateConnection();
        return await connection.ExecuteScalarAsync<bool>(
            "SELECT EXISTS(SELECT 1 FROM Tags WHERE Id = @Id)", new { Id = id });
    }
}